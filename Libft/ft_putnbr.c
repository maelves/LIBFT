#include "libft.h"

void	ft_putnbr (int n)
{
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putstr("214748");
		ft_putstr("3648");
	}
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			ft_putnbr(-n);
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putnbr (n % 10 + '0');
		}
		if (n >= 0 && n <= 9)
		{
			ft_putchar(n + '0');
		}
	}
}
